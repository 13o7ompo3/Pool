int	ft_sqrt(int nb)
{
	int	k;

	if (nb == 1)
		return (1);
	if (nb <= 0)
		return (0);
	k = 2;
	while (k * k < nb)
		k++;
	if (k * k != nb)
		return (0);
	return (k);
}
